import Foundation

enum AccessibilityIds {

    enum Post {
        static let title = "post.title"
        static let subtitle = "post.subtitle"
    }

    enum Details {
        static let authorLabel = "details.author.label"
        static let authorValue = "details.author.value"
        static let emailLabel = "details.email.label"
        static let emailValue = "details.email.value"

        static let numberOfComments = "details.numberOfComments"
        static let body = "details.body"
    }
}
